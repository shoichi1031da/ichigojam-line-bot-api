const IchigoJamDecoder = (msgCharaCode) => {
    
    let decodedMSG = "";

    for(const i in msgCharaCode){

        if(msgCharaCode[i] > 128 && msgCharaCode[i] < 256){
            decodedMSG += decodedStrings[msgCharaCode[i] - 129];
        } else{
            decodedMSG += String.fromCharCode(msgCharaCode[i]);
        } 
    }

    return decodedMSG;
   
}

